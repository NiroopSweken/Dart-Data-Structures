void main() {
  for (int i = 0; i <= 100; i++) {
    /*-----------------------------------------------------*/
    /*---Print FizzBuzz if number is divisible by 3 and 5--*/
    /*-----------------------------------------------------*/
    if (i % 3 == 0 && i % 5 == 0) {
      print("FizzBuzz");
    }

    /*-----------------------------------------------------*/
    /*-------Print Fizz if number is divisible by 3--------*/
    /*-----------------------------------------------------*/
    else if (i % 3 == 0) {
      print("Fizz");
    }

    /*-----------------------------------------------------*/
    /*-------Print Buzz if number is divisible by 5--------*/
    /*-----------------------------------------------------*/
    else if (i % 5 == 0) {
      print("Buzz");
    }

    /*-----------------------------------------------------*/
    /*-------------Else just Print the number--------------*/
    /*-----------------------------------------------------*/
    else {
      print(i);
    }
  }
}
